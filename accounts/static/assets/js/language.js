function setLanguage(langCode) {
    const elements = document.querySelectorAll('[data-lang]');
    elements.forEach(el => {
        const key = el.getAttribute('data-lang');
        if (lang[langCode] && lang[langCode][key]) {
            el.textContent = lang[langCode][key];
        }
    });

    // تغيير الاتجاه والخط
    document.body.classList.remove('lang-ar', 'lang-en');
    document.body.classList.add(langCode === 'ar' ? 'lang-ar' : 'lang-en');

    // ✅ تعديل الاتجاه واللغة داخل سكشن #home فقط
    const homeSection = document.getElementById('home');
    if (homeSection) {
        if (langCode === 'ar') {
            homeSection.setAttribute('dir', 'rtl');
            homeSection.setAttribute('lang', 'ar');
        } else {
            homeSection.setAttribute('dir', 'ltr');
            homeSection.setAttribute('lang', 'en');
        }
    }
}

// تغيير اللغة عند الضغط على الزر
document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        const selectedLang = btn.getAttribute('data-lang-btn');
        setLanguage(selectedLang);
        localStorage.setItem('selectedLang', selectedLang);

        // ✅ إغلاق القائمة بعد الاختيار
        const menu = document.getElementById('currencyMenu');
        if (menu) {
            menu.classList.remove('show');
        }
    });
});

